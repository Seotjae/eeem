package gudi.pro.eeem.dto;

import java.util.Date;

import org.apache.ibatis.type.Alias;

@Alias("meetReview")
public class ReviewDTO {
	
	private int rev_num; //글번호
	private int meet_num; //모임번호
	private String mem_id; //작성자
	private String rev_subject; //제목
	private String rev_content; //내용
	private Date rev_date; //날짜
	
	public int getRev_num() {
		return rev_num;
	}
	public void setRev_num(int rev_num) {
		this.rev_num = rev_num;
	}
	public int getMeet_num() {
		return meet_num;
	}
	public void setMeet_num(int meet_num) {
		this.meet_num = meet_num;
	}
	public String getMem_id() {
		return mem_id;
	}
	public void setMem_id(String mem_id) {
		this.mem_id = mem_id;
	}
	public String getRev_subject() {
		return rev_subject;
	}
	public void setRev_subject(String rev_subject) {
		this.rev_subject = rev_subject;
	}
	public String getRev_content() {
		return rev_content;
	}
	public void setRev_content(String rev_content) {
		this.rev_content = rev_content;
	}
	public Date getRev_date() {
		return rev_date;
	}
	public void setRev_date(Date rev_date) {
		this.rev_date = rev_date;
	}
	
	
}
